/**
 * Generates `docs/reference/endpoint-doc-index.md` from
 * `internal/docs-i18n-inventory.json`. The page is a deterministic
 * "endpoint URL → canonical doc page" lookup, grouped by region/topic,
 * which both helps users find pages by raw API path and feeds the
 * local search index with dense, exact-match text.
 *
 * Run: node scripts/generate-endpoint-doc-index.js
 *      (also runs as part of npm `prebuild`).
 */

const fs = require("fs");
const path = require("path");

const { aliasesFor, aliasesForMany } = require("./lib/endpoint-aliases");

const ROOT = path.join(__dirname, "..");
const INVENTORY_PATH = path.join(ROOT, "internal", "docs-i18n-inventory.json");
const DOCS_ROOT = path.join(ROOT, "docs");
const OUT_PATH = path.join(DOCS_ROOT, "reference", "endpoint-doc-index.md");
const SYNONYMS_PATH = path.join(ROOT, "internal", "search-synonyms.json");

const COUNTRY_SEGMENTS = new Map([
	["co", "Colombia"],
	["mx", "Mexico"],
	["br", "Brazil"],
	["ar", "Argentina"],
	["cl", "Chile"],
	["pe", "Peru"],
	["ec", "Ecuador"],
	["ve", "Venezuela"],
	["pa", "Panama"],
	["gt", "Guatemala"],
	["cr", "Costa Rica"],
	["do", "Dominican Republic"],
	["hn", "Honduras"],
	["ni", "Nicaragua"],
	["sv", "El Salvador"],
	["bo", "Bolivia"],
	["uy", "Uruguay"],
	["py", "Paraguay"],
	["us", "United States"],
	["es", "Spain"],
	["in", "India"],
]);

const readFrontMatter = (absPath) => {
	if (!fs.existsSync(absPath)) return {};
	const raw = fs.readFileSync(absPath, "utf8");
	if (!raw.startsWith("---")) return {};
	const end = raw.indexOf("\n---", 3);
	if (end === -1) return {};
	const block = raw.slice(3, end);
	const fm = {};
	for (const line of block.split("\n")) {
		const match = /^\s*([A-Za-z0-9_]+)\s*:\s*(.*)$/.exec(line);
		if (!match) continue;
		const key = match[1];
		let value = match[2].trim();
		if (value.startsWith('"') && value.endsWith('"')) value = value.slice(1, -1);
		fm[key] = value;
	}
	return fm;
};

const groupFor = (endpointUrl) => {
	const parts = endpointUrl.split("/").filter(Boolean);
	if (parts.length === 0) return { key: "other", label: "Other" };
	if (parts[0] === "v2" && parts.length > 1) {
		const seg = parts[1].toLowerCase();
		const country = COUNTRY_SEGMENTS.get(seg);
		if (country) return { key: `country-${seg}`, label: country, sort: `1-${seg}` };
		return { key: `topic-${seg}`, label: titleCase(seg.replace(/-/g, " ")), sort: `2-${seg}` };
	}
	if (parts[0] === "api") return { key: "api-zelf", label: "Zelf / API", sort: "3-api" };
	return { key: parts[0], label: titleCase(parts[0]), sort: `4-${parts[0]}` };
};

const titleCase = (s) =>
	String(s)
		.split(" ")
		.map((w) => (w ? w[0].toUpperCase() + w.slice(1) : w))
		.join(" ");

const main = () => {
	if (!fs.existsSync(INVENTORY_PATH)) {
		console.error(`Inventory not found at ${INVENTORY_PATH}. Run scripts/inventory-docs-endpoints.js first.`);
		process.exit(1);
	}

	const inventory = JSON.parse(fs.readFileSync(INVENTORY_PATH, "utf8"));
	const synonyms = fs.existsSync(SYNONYMS_PATH) ? JSON.parse(fs.readFileSync(SYNONYMS_PATH, "utf8")) : { countries: [], products: [], documents: [] };

	const groups = new Map();

	for (const row of inventory.canonical || []) {
		const fm = readFrontMatter(path.join(DOCS_ROOT, row.primaryPath));
		const dir = path.dirname(row.primaryPath);
		const baseName = path.basename(row.primaryPath).replace(/\.(md|mdx)$/i, "");
		const slug = fm.slug
			? fm.slug
			: fm.id
				? `/${dir === "." ? fm.id : `${dir}/${fm.id}`}`
				: `/${row.primaryPath.replace(/\.(md|mdx)$/i, "")}`;
		const title = fm.title || baseName;

		const group = groupFor(row.endpointUrl);
		if (!groups.has(group.key)) groups.set(group.key, { ...group, rows: [] });
		groups.get(group.key).rows.push({
			endpointUrl: row.endpointUrl,
			slug,
			title,
			primaryPath: row.primaryPath,
		});
	}

	const sortedGroups = [...groups.values()].sort((a, b) => String(a.sort || a.key).localeCompare(String(b.sort || b.key)));

	const allEndpoints = (inventory.canonical || []).map((row) => row.endpointUrl);
	const globalAliases = aliasesForMany(allEndpoints);

	let md = "";
	md += "---\n";
	md += "id: endpoint-doc-index\n";
	md += 'title: "Endpoint URL → Doc page (Reference)"\n';
	md += "description: Deterministic mapping from each Verifik API endpoint path to its canonical documentation page, grouped by country and topic.\n";
	md += "slug: /reference/endpoint-doc-index\n";
	md += "sidebar_label: Endpoint Index\n";
	md += "keywords:\n";
	md += "  - endpoint index\n";
	md += "  - api reference\n";
	md += "  - country lookup\n";
	md += "  - url to doc\n";
	for (const country of synonyms.countries || []) {
		for (const name of country.names || []) md += `  - ${name}\n`;
	}
	for (const alias of globalAliases) md += `  - ${JSON.stringify(alias)}\n`;
	md += "---\n\n";

	md += "<!-- AUTOGENERATED by scripts/generate-endpoint-doc-index.js. Do not edit by hand. -->\n\n";
	md += "# Endpoint URL → documentation page\n\n";
	md += `This page lists every Verifik API endpoint that has a canonical doc page (${inventory.uniqueEndpointUrls || (inventory.canonical || []).length} entries),\n`;
	md += "grouped by country or topic. Use it when you have a raw URL fragment (e.g. `v2/co/cedula`) and want to jump to the documentation.\n\n";
	md += "If the search bar didn't find your endpoint, this page lists every API path along with whitespace-tokenized aliases so it shows up regardless of how you typed the URL.\n\n";
	md += `_Last regenerated: ${new Date().toISOString()}_\n\n`;

	for (const group of sortedGroups) {
		md += `## ${group.label}\n\n`;
		md += "| Endpoint | Aliases | Page |\n";
		md += "| --- | --- | --- |\n";
		group.rows.sort((a, b) => a.endpointUrl.localeCompare(b.endpointUrl));
		for (const r of group.rows) {
			const prettyTitle = r.title.replace(/\|/g, "\\|");
			const aliasCell = aliasesFor(r.endpointUrl)
				.filter((alias) => alias !== r.endpointUrl && alias !== `/${r.endpointUrl}`)
				.filter((alias) => !/[{}]/.test(alias))
				.join(" ")
				.replace(/\|/g, "\\|");
			md += `| \`${r.endpointUrl}\` | ${aliasCell} | [${prettyTitle}](${r.slug}) |\n`;
		}
		md += "\n";
	}

	if (!fs.existsSync(path.dirname(OUT_PATH))) fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
	fs.writeFileSync(OUT_PATH, md, "utf8");
	console.log(`Wrote ${path.relative(ROOT, OUT_PATH)} with ${sortedGroups.reduce((n, g) => n + g.rows.length, 0)} rows in ${sortedGroups.length} groups`);
};

main();
